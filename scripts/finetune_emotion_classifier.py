# Emotion labels (Plutchik's 8 basic emotions from MilaNLProc/xlm-emo-t)
LABEL2ID = {
    "anger": 0,
    "anticipation": 1,
    "disgust": 2,
    "fear": 3,
    "joy": 4,
    "sadness": 5,
    "surprise": 6,
    "trust": 7,
}
ID2LABEL = {v: k for k, v in LABEL2ID.items()}
EMOTIONS = list(LABEL2ID.keys())


def get_device() -> str:
    """Detect best available device."""
    if torch.cuda.is_available():
        return "cuda"
    elif hasattr(torch.backends, "mps") and torch.backends.mps.is_available():
        return "mps"
    return "cpu"


def detect_runtime(num_workers_arg: int = -1) -> dict[str, Any]:
    """Detect runtime configuration based on available hardware."""
    device = get_device()

    if device == "cuda":
        torch.backends.cuda.matmul.allow_tf32 = True
        torch.backends.cudnn.allow_tf32 = True
        torch.backends.cudnn.benchmark = True
        pin_memory = True
        auto_workers = min(8, os.cpu_count() or 1)
        bf16 = torch.cuda.is_bf16_supported()
        fp16 = not bf16
    elif device == "mps":
        pin_memory = False
        auto_workers = 0
        bf16 = False
        fp16 = False
    else:
        pin_memory = False
        auto_workers = min(4, os.cpu_count() or 1)
        bf16 = False
        fp16 = False

    num_workers = auto_workers if num_workers_arg < 0 else num_workers_arg

    return {
        "device": device,
        "pin_memory": pin_memory,
        "num_workers": num_workers,
        "bf16": bf16,
        "fp16": fp16,
    }


def set_seed(seed: int) -> None:
    """Set random seeds for reproducibility."""
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


def check_mlflow_env() -> bool:
    """Check that required MLflow environment variables are set."""
    required_vars = [
        "MLFLOW_TRACKING_URI",
        "MLFLOW_TRACKING_USERNAME",
        "MLFLOW_TRACKING_PASSWORD",
    ]
    missing = [v for v in required_vars if not os.getenv(v)]
    if missing:
        logger.warning(f"Missing MLflow environment variables: {missing}")
        return False
    return True


def compute_metrics(eval_pred) -> dict[str, float]:
    """Compute accuracy, F1, precision, recall for evaluation."""
    accuracy_metric = evaluate.load("accuracy")
    f1_metric = evaluate.load("f1")
    precision_metric = evaluate.load("precision")
    recall_metric = evaluate.load("recall")

    predictions, labels = eval_pred
    predictions = np.argmax(predictions, axis=1)

    acc = accuracy_metric.compute(predictions=predictions, references=labels)
    f1 = f1_metric.compute(predictions=predictions, references=labels, average="weighted")
    f1_macro = f1_metric.compute(predictions=predictions, references=labels, average="macro")
    precision = precision_metric.compute(predictions=predictions, references=labels, average="weighted")
    recall = recall_metric.compute(predictions=predictions, references=labels, average="weighted")

    return {
        "accuracy": acc["accuracy"],
        "f1_weighted": f1["f1"],
        "f1_macro": f1_macro["f1"],
        "precision": precision["precision"],
        "recall": recall["recall"],
    }


def plot_confusion_matrix(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    labels: list[str],
    output_path: Path,
    normalize: bool = True,
) -> Path:
    """Generate and save confusion matrix plot."""
    cm = confusion_matrix(y_true, y_pred, normalize="true" if normalize else None)

    plt.figure(figsize=(12, 10))
    sns.heatmap(
        cm,
        annot=True,
        fmt=".2f" if normalize else "d",
        cmap="Blues",
        xticklabels=labels,
        yticklabels=labels,
    )
    plt.xlabel("Predicted")
    plt.ylabel("True")
    plt.title("Emotion Classification Confusion Matrix")
    plt.tight_layout()

    plot_path = output_path / "confusion_matrix.png"
    plt.savefig(plot_path, dpi=150)
    plt.close()

    return plot_path


def plot_class_distribution(
    df: pd.DataFrame,
    emotion_col: str,
    output_path: Path,
    title: str = "Emotion Distribution",
) -> Path:
    """Plot emotion class distribution."""
    plt.figure(figsize=(10, 6))
    counts = df[emotion_col].value_counts()
    sns.barplot(x=counts.index, y=counts.values, palette="viridis")
    plt.xlabel("Emotion")
    plt.ylabel("Count")
    plt.title(title)
    plt.xticks(rotation=45)
    plt.tight_layout()

    plot_path = output_path / "class_distribution.png"
    plt.savefig(plot_path, dpi=150)
    plt.close()

    return plot_path


def evaluate_on_test_set(
    trainer: Trainer,
    test_dataset: Dataset,
    output_dir: Path,
) -> dict[str, Any]:
    """Run comprehensive evaluation on test set."""
    logger.info("Running evaluation on test set...")

    # Get predictions
    predictions_output = trainer.predict(test_dataset)
    predictions = np.argmax(predictions_output.predictions, axis=1)
    labels = predictions_output.label_ids

    # Classification report
    report = classification_report(
        labels,
        predictions,
        target_names=EMOTIONS,
        output_dict=True,
    )

    # Save classification report
    report_path = output_dir / "classification_report.json"
    with open(report_path, "w") as f:
        json.dump(report, f, indent=2)
    logger.info(f"Classification report saved to {report_path}")

    # Print classification report
    print("\n" + "=" * 60)
    print("CLASSIFICATION REPORT")
    print("=" * 60)
    print(classification_report(labels, predictions, target_names=EMOTIONS))

    # Confusion matrix
    cm_path = plot_confusion_matrix(
        labels,
        predictions,
        EMOTIONS,
        output_dir,
    )
    logger.info(f"Confusion matrix saved to {cm_path}")

    # Per-class metrics
    per_class_metrics = {}
    for emotion in EMOTIONS:
        if emotion in report:
            per_class_metrics[emotion] = {
                "precision": report[emotion]["precision"],
                "recall": report[emotion]["recall"],
                "f1": report[emotion]["f1-score"],
                "support": report[emotion]["support"],
            }

    return {
        "test_accuracy": report["accuracy"],
        "test_f1_weighted": report["weighted avg"]["f1-score"],
        "test_f1_macro": report["macro avg"]["f1-score"],
        "test_precision_weighted": report["weighted avg"]["precision"],
        "test_recall_weighted": report["weighted avg"]["recall"],
        "per_class_metrics": per_class_metrics,
        "classification_report_path": str(report_path),
        "confusion_matrix_path": str(cm_path),
    }


def log_to_mlflow(
    args: argparse.Namespace,
    runtime: dict[str, Any],
    data_stats: dict[str, Any],
    training_time: float,
    test_metrics: dict[str, Any],
    output_path: Path,
    run_name: Optional[str] = None,
) -> str:
    """Log all training artifacts to MLflow."""
    logger.info("Logging to MLflow...")

    mlflow.set_tracking_uri(os.getenv("MLFLOW_TRACKING_URI", "https://mlflow.yousef.gg"))
    mlflow.set_experiment(args.experiment_name)

    with mlflow.start_run(run_name=run_name):
        # Log hyperparameters
        mlflow.log_params({
            "base_model": args.base_model,
            "batch_size": args.batch_size,
            "epochs": args.epochs,
            "learning_rate": args.learning_rate,
            "max_length": args.max_length,
            "max_samples_per_class": args.max_samples_per_class,
            "test_size": args.test_size,
            "val_size": args.val_size,
            "min_confidence": args.min_confidence,
            "warmup_ratio": args.warmup_ratio,
            "weight_decay": args.weight_decay,
            "early_stopping_patience": args.early_stopping_patience,
            "seed": args.seed,
        })

        # Log runtime config
        mlflow.log_params({
            "device": runtime["device"],
            "bf16": runtime["bf16"],
            "fp16": runtime["fp16"],
            "num_workers": runtime["num_workers"],
        })

        # Log data statistics
        mlflow.log_params({
            "total_samples": data_stats["total_samples"],
            "train_samples": data_stats["train_samples"],
            "val_samples": data_stats["val_samples"],
            "test_samples": data_stats["test_samples"],
        })

        # Log training time
        mlflow.log_metric("training_time_seconds", training_time)

        # Log test metrics
        for metric_name, value in test_metrics.items():
            if isinstance(value, (int, float)):
                mlflow.log_metric(metric_name, float(value))

        # Log per-class metrics
        if "per_class_metrics" in test_metrics:
            for emotion, metrics in test_metrics["per_class_metrics"].items():
                for metric_name, value in metrics.items():
                    if isinstance(value, (int, float)):
                        mlflow.log_metric(f"{emotion}_{metric_name}", float(value))

        # Log artifacts
        artifacts_to_log = [
            ("confusion_matrix.png", "plots"),
            ("class_distribution.png", "plots"),
            ("classification_report.json", "metrics"),
            ("training_config.json", "config"),
            ("data_stats.json", "stats"),
        ]

        for filename, artifact_path in artifacts_to_log:
            file_path = output_path / filename
            if file_path.exists():
                try:
                    mlflow.log_artifact(str(file_path), artifact_path=artifact_path)
                except Exception as e:
                    logger.warning(f"Could not log artifact {filename}: {e}")

        # Log model directory
        final_model_path = output_path / "final"
        if final_model_path.exists():
            try:
                mlflow.log_artifacts(str(final_model_path), artifact_path="model")
            except Exception as e:
                logger.warning(f"Could not log model artifacts: {e}")

        # Set tags
        mlflow.set_tags({
            "model_type": "EmotionClassifier",
            "framework": "transformers",
            "task": "text-classification",
            "training_date": datetime.now().isoformat(),
            "base_model": args.base_model,
        })

        run_id = mlflow.active_run().info.run_id
        logger.info(f"MLflow run ID: {run_id}")

        return run_id


