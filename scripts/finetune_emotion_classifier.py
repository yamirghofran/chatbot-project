"""
Fine-tune a multilingual DistilBERT for emotion classification on book reviews.

Uses pseudo-labels generated by MilaNLProc/xlm-emo-t (knowledge distillation).
Supports multiple languages since reviews are not all in English.

Features:
- MLflow experiment tracking
- Early stopping
- Confusion matrix visualization
- Per-class metrics (precision, recall, F1)
- Checkpoint management
- Reproducible training with seed control
"""

