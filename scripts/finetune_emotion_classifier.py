def main() -> None:
    args = parse_args()

    # Set seed for reproducibility
    set_seed(args.seed)

    # Detect runtime
    runtime = detect_runtime(args.num_workers)
    logger.info(f"Runtime: device={runtime['device']} bf16={runtime['bf16']} fp16={runtime['fp16']}")

    # Paths
    base_dir = Path(__file__).parent.parent
    input_path = base_dir / args.input
    output_dir = base_dir / args.output_dir
    output_dir.mkdir(parents=True, exist_ok=True)

    # Load data
    logger.info(f"Loading labeled reviews from {input_path}...")
    df = pd.read_parquet(input_path)
    logger.info(f"Total reviews: {len(df):,}")

    # Filter by confidence
    df = df[df['emotion_score'] >= args.min_confidence]
    logger.info(f"After confidence filter (>= {args.min_confidence}): {len(df):,}")

    # Filter out empty texts
    df = df[df['review_text'].notna() & (df['review_text'].str.strip() != '')]
    logger.info(f"After removing empty texts: {len(df):,}")

    # Map emotions to labels
    df['label'] = df['emotion'].map(LABEL2ID)

    # Check for unmapped labels
    if df['label'].isna().any():
        unmapped = df[df['label'].isna()]['emotion'].unique()
        logger.warning(f"Unmapped emotions: {unmapped}")
        df = df.dropna(subset=['label'])

    df['label'] = df['label'].astype(int)

    # Split test set FIRST from original distribution (reflects real-world deployment)
    train_val_df, test_df = train_test_split(
        df, test_size=args.test_size, random_state=args.seed, stratify=df['label']
    )
    logger.info(f"Test set (original distribution): {len(test_df):,} samples")
    logger.info(f"Test set distribution:\n{test_df['emotion'].value_counts()}")

    # Balance training data: cap classes at max_samples_per_class
    max_per_class = args.max_samples_per_class
    logger.info(f"Balancing train/val data (max {max_per_class:,} per class)...")
    train_val_df = train_val_df.groupby('emotion').apply(
        lambda x: x.sample(min(len(x), max_per_class), random_state=args.seed)
    ).reset_index(drop=True)
    logger.info(f"After balancing train/val: {len(train_val_df):,} reviews")
    logger.info(f"Train/val distribution:\n{train_val_df['emotion'].value_counts()}")

    # Split train/val
    train_df, val_df = train_test_split(
        train_val_df, test_size=args.val_size, random_state=args.seed, stratify=train_val_df['label']
    )

    # Plot class distribution (training data)
    dist_plot_path = plot_class_distribution(train_df, 'emotion', output_dir, "Training Data Emotion Distribution")
    logger.info(f"Class distribution plot saved to {dist_plot_path}")

    data_stats = {
        "total_samples": len(df),
        "train_samples": len(train_df),
        "val_samples": len(val_df),
        "test_samples": len(test_df),
        "emotion_distribution": df['emotion'].value_counts().to_dict(),
    }

    logger.info(f"Split sizes: Train={len(train_df):,} Val={len(val_df):,} Test={len(test_df):,}")

    # Save data stats
    stats_path = output_dir / "data_stats.json"
    with open(stats_path, "w") as f:
        json.dump(data_stats, f, indent=2)

    # Convert to HuggingFace datasets
    def to_dataset(dataframe: pd.DataFrame) -> Dataset:
        return Dataset.from_pandas(
            dataframe[['review_text', 'label']].rename(columns={'review_text': 'text'}),
            preserve_index=False
        )

    dataset = DatasetDict({
        'train': to_dataset(train_df),
        'validation': to_dataset(val_df),
        'test': to_dataset(test_df),
    })

    # Load tokenizer and model
    logger.info(f"Loading tokenizer and model: {args.base_model}")
    tokenizer = AutoTokenizer.from_pretrained(args.base_model)

    def tokenize(examples):
        return tokenizer(
            examples['text'],
            truncation=True,
            max_length=args.max_length,
            padding=False,
        )

    logger.info("Tokenizing datasets...")
    tokenized_dataset = dataset.map(tokenize, batched=True, remove_columns=['text'])

    # Load model
    model = AutoModelForSequenceClassification.from_pretrained(
        args.base_model,
        num_labels=len(LABEL2ID),
        id2label=ID2LABEL,
        label2id=LABEL2ID,
    )

    # Data collator
    data_collator = DataCollatorWithPadding(tokenizer=tokenizer)

    # Training arguments
    training_args = TrainingArguments(
        output_dir=str(output_dir / "checkpoints"),
        learning_rate=args.learning_rate,
        per_device_train_batch_size=args.batch_size,
        per_device_eval_batch_size=args.batch_size * 2,
        num_train_epochs=args.epochs,
        weight_decay=args.weight_decay,
        eval_strategy="steps",
        eval_steps=args.eval_steps,
        save_strategy="steps",
        save_steps=args.save_steps,
        save_total_limit=args.save_total_limit,
        load_best_model_at_end=True,
        metric_for_best_model="f1_macro",
        greater_is_better=True,
        logging_steps=args.logging_steps,
        warmup_ratio=args.warmup_ratio,
        bf16=runtime["bf16"],
        fp16=runtime["fp16"],
        dataloader_num_workers=runtime["num_workers"],
        dataloader_pin_memory=runtime["pin_memory"],
        report_to="none",
        seed=args.seed,
    )

    # Callbacks
    callbacks = []
    if args.early_stopping_patience > 0:
        callbacks.append(
            EarlyStoppingCallback(early_stopping_patience=args.early_stopping_patience)
        )

    # Trainer
    trainer = Trainer(
        model=model,
        args=training_args,
        train_dataset=tokenized_dataset['train'],
        eval_dataset=tokenized_dataset['validation'],
        #tokenizer=tokenizer,
        data_collator=data_collator,
        compute_metrics=compute_metrics,
        callbacks=callbacks,
    )

    # Save training config
    config = {
        "base_model": args.base_model,
        "max_length": args.max_length,
        "batch_size": args.batch_size,
        "epochs": args.epochs,
        "learning_rate": args.learning_rate,
        "warmup_ratio": args.warmup_ratio,
        "weight_decay": args.weight_decay,
        "early_stopping_patience": args.early_stopping_patience,
        "seed": args.seed,
        "emotions": EMOTIONS,
        "label2id": LABEL2ID,
        "id2label": ID2LABEL,
        "runtime": runtime,
    }
    config_path = output_dir / "training_config.json"
    with open(config_path, "w") as f:
        json.dump(config, f, indent=2, default=str)

    # Train
    logger.info("Starting training...")
    print("\n" + "=" * 60)
    print("TRAINING CONFIGURATION")
    print("=" * 60)
    print(json.dumps(config, indent=2, default=str))
    print("=" * 60 + "\n")

    start_time = time.time()
    trainer.train()
    training_time = time.time() - start_time
    logger.info(f"Training completed in {training_time:.2f} seconds ({training_time/60:.2f} minutes)")

    # Evaluate on test set
    test_metrics = evaluate_on_test_set(trainer, tokenized_dataset['test'], output_dir)

    # Save final model
    final_model_path = output_dir / "final"
    trainer.save_model(str(final_model_path))
    tokenizer.save_pretrained(str(final_model_path))
    logger.info(f"Model saved to {final_model_path}")

    # Save test set for later evaluation
    test_df.to_parquet(output_dir / "test_set.parquet", index=False)

    # Save run summary
    run_summary = {
        "training_time_seconds": training_time,
        "training_time_minutes": training_time / 60,
        "data_stats": data_stats,
        "test_metrics": test_metrics,
        "config": config,
        "model_path": str(final_model_path),
        "timestamp": datetime.now().isoformat(),
    }
    summary_path = output_dir / "run_summary.json"
    with open(summary_path, "w") as f:
        json.dump(run_summary, f, indent=2, default=str)
    logger.info(f"Run summary saved to {summary_path}")

    # Log to MLflow
    mlflow_run_id = None
    if not args.skip_mlflow and check_mlflow_env():
        run_name = f"emotion_clf_{args.base_model.split('/')[-1]}_{datetime.now().strftime('%Y%m%d_%H%M%S')}"
        try:
            mlflow_run_id = log_to_mlflow(
                args=args,
                runtime=runtime,
                data_stats=data_stats,
                training_time=training_time,
                test_metrics=test_metrics,
                output_path=output_dir,
                run_name=run_name,
            )
        except Exception as e:
            logger.warning(f"MLflow logging failed: {e}")

    # Print final summary
    print("\n" + "=" * 60)
    print("TRAINING COMPLETE")
    print("=" * 60)
    print(f"Training time: {training_time/60:.2f} minutes")
    print(f"Test accuracy: {test_metrics['test_accuracy']:.4f}")
    print(f"Test F1 (weighted): {test_metrics['test_f1_weighted']:.4f}")
    print(f"Test F1 (macro): {test_metrics['test_f1_macro']:.4f}")
